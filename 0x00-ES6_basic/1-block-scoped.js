/*
Modified variables (all formerly declared with 'var') in *initial function*
so they aren't overwritten inside the conditional block
*/
export default function taskBlock(trueOrFalse) {
  const task = false;
  const task2 = true;

  if (trueOrFalse) {
    const task = true;
    const task2 = false;
  }

  return [task, task2];
}

